#ifndef AVRBONG_H_
#define AVRBONG_H_

#include <stdint.h>
#include <stdbool.h>

/************************************************************************/
/* Some helpers                                                         */
/************************************************************************/

#define lobyte(s) ((s) & 0xff)
#define hibyte(s) (((s) >> 8) & 0xff)
#define makeu16(hi, lo) (((hi) << 8) | (lo))

/************************************************************************/
/* Serial                                                               */
/************************************************************************/

void s_open();
bool s_avail();
uint8_t s_getch();
void s_putch();

/************************************************************************/
/* SPI                                                                  */
/************************************************************************/

void spi_open();
uint8_t spi_transaction(uint8_t a, uint8_t b, uint8_t c, uint8_t d);

#endif /* AVRBONG_H_ */
